type EthAPI struct {
	c *rpc.Client
}

func Dial(rawurl string) (*EthAPI, error) {
	return DialContext(context.Background(), rawurl)
}

func DialContext(ctx context.Context, rawurl string) (*EthAPI, error) {
	c, err := rpc.DialContext(ctx, rawurl)
	if err != nil {
		return nil, err
	}
	return NewClient(c), nil
}

func NewClient(c *rpc.Client) *EthAPI {
	return &EthAPI{c}
}

func (ec *EthAPI) Close() {
	ec.c.Close()
}

